#pragma once

struct CustomString;

struct Operation
{
    // ----------------------------------------------------------------------------

    enum Type
    {
        // Not an operation
        NAO,
        MIN,
        MAX,
        // Opening bracket -> (
        OB,
        // Closing bracket -> )
        CB,
        // Next argument
        NA
    };

    // ----------------------------------------------------------------------------

    Operation(Type type);

    // ----------------------------------------------------------------------------

    static Type DetermineType(const CustomString* str);

    // ----------------------------------------------------------------------------

    int GetArgCount() const;

    // ----------------------------------------------------------------------------

    Type GetType() const;

    // ----------------------------------------------------------------------------

    void IncrementArgCount();

    // ----------------------------------------------------------------------------

    void AddToStr(CustomString& str) const;

    // ----------------------------------------------------------------------------

private:

    // ----------------------------------------------------------------------------

    void AddType(CustomString& str) const;

    // ----------------------------------------------------------------------------
    Type m_type;
    int m_argCount;

    // ----------------------------------------------------------------------------
};